// Creamos dos arreglos de strings
let frutas = ["manzana", "pera", "fresa"];
let vegetales = ["lechuga", "coliflor", "zanahoria"];

// Concatenamos los dos arreglos
let alimentos = frutas.concat(vegetales);

// Imprimimos el arreglo resultante
console.log("Arreglo concatenado: "+alimentos);

// La salida en consola es: Arreglo concatenado: manzana,pera,fresa,lechuga,coliflor,zanahoria